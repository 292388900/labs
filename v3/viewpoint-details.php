<h3>泡汤六步曲：</h3>
<p>
1、淋浴：汤客泡温泉之前，请先用香波浴液等淋浴洁身，共同保持温泉的水质纯净。<br>
2、试温：选择适合自己舒适度的温泉池沐浴，一般由低到高温。<br>
人体最舒适的温泉温度为38oC至42oC。除少量特色温泉泡池，本温泉的温度都保持在上述最佳泡泉温度之中。<br>
3、体验：一般先用手感受温泉泉质及温度，用手地将温泉水泼淋全身，依次再将脚、腿、身体浸入温泉，逐步体味温泉给您带来的舒适感受。适当的穴位按摩可提高温泉的保健功效。<br>
4、水位：浸泡温泉的高低水位以低于胸口为宜。<br>
5、时间：每次泡泉不要超过15分钟，应上岸稍作休息5分钟以上，再行泡泉。如发现口干或胸闷等不适应感觉，可上岸饮水缓解。<br>
6、清洗：部分泡池流入的是未经矿物过滤的温泉原汤，如对皮肤浴后湿滑不适应，可用更淋区清水冲洗；建议此时不必再用香皂或浴液，以利皮肤表面保存吸收的温泉矿物质。
</p>

