<h3>泉-品质</h3>
<p>
恐龙谷温泉采自地表以下2009.70米深处，距今约2.5亿年前的三叠纪地层。日涌泉量达500立方米，井底温度为82℃，终年出水温度超过55℃。泉质特点为偏硅酸？锶温泉（标准的美人汤+强骨汤+清脂汤）。</p>
<p>
经国土资源部南京矿产资源监督检测中心检测得出：恐龙谷温泉的泉质堪称顶级,温泉中矿物质含量极其丰富，尤足珍稀的是锶、偏硅酸等含量超过国际“医疗热矿泉标准”浓度和国家饮用天然矿泉水标准。以偏硅酸形式存在的硅元素对皮肤能起到清洁作用，具有软化皮肤角质、防衰老、使皮肤光滑等功效，常泡偏硅酸型温泉具有“温泉水滑洗凝脂”的神奇效果。同时，硅元素也是人体必需的微量元素，对心血管、骨骼生长等具有保健功能。锶则不仅具有改善心血管、增强神经及肌肉的兴奋性等功能，对冠心病及儿童身材发育迟滞有改善辅助作用。另外，恐龙谷温泉水中所含的氟、铁、镁、碘、铜、钼、溴等元素对慢性支气管炎、哮喘、便秘、胃痉挛、失眠等慢性疾病也有理疗效果。</p>

